let theBall = [];

function setup() {
  createCanvas(windowWidth, windowHeight);
}

function draw() {
  background(220);
  maincharactercharacteristics();
}

function maincharactercharacteristics() {
  let ball = {
    x: width/2,
    y: height,
    diameter: 50
  };

  theBall.push(ball);
}
